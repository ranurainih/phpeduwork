<?php 
	// Luas Persegi
	$sisi = 3;
	$luaspersegi = $sisi * $sisi;
	echo "Luas persegi dengan sisi $sisi adalah $luaspersegi <br>";

	// Luas Persegi Panjang
	$p = 5;
	$l = 3;
	$luaspersegipanjang = $p * $l;
	echo "Luas persegi panjang dengan panjang $p dan lebar $l adalah $luaspersegipanjang <br>";

	// Luas Segitiga
	$a = 3;
	$t = 9;
	$luassegitiga = 1/2 * $a * $t;
	echo "Luas segitiga dengan alas $a dan tinggi $t adalah $luassegitiga <br>";

	// Luas Lingkaran
	$r = 14;
	$luaslingkaran = 3.14 * $r * $r;
	echo "Luas lingkaran dengan jari-jari $r adalah $luaslingkaran <br>";

	// Luas Jajar Genjang
	$alas = 3;
	$tinggi = 4;
	$luasjajargenjang = $alas * $tinggi;
